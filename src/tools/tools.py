def get_current_weather(location, unit="fahrenheit"):
    """Function to get the current weather in a given location"""
    if unit == "celsius":
        temperature = random.randint(-34, 43)
    else:
        temperature = random.randint(-30, 110)

    return {
        "temperature": temperature,
        "unit": unit,
        "location": location,
    }


